#Dictionaries
my_dict = {
    'name': 'Tim',
    'nationality': 'African',
    'age': 24,
    'isTall' : True,
    'qualification': 'College',
    'friends': ['Peter', 'Paul', 'Precious']
}

#print whole dictionary
print(my_dict)

#length - the length is given by the amount of key/value pairs
print(len(my_dict))

#print only a value
print(my_dict['name'])

#print the type of the dictionary
print(type(my_dict))

#assign the value of a key to a variable
x = my_dict['name']
print(x)